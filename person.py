class Person:
    def __init__(self,
                 second_name: str,
                 name: str,
                 third_name: str,
                 organization: str,
                 work_phone: str,
                 personal_number: str
                 ):
        self.name = name
        self.second_name = second_name
        self.third_name = third_name
        self.organization = organization
        self.work_phone = work_phone
        self.personal_number = personal_number

